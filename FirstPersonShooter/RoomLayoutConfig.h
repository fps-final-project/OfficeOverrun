#pragma once

namespace WorldGenerator
{
	class __declspec(dllexport) RoomLayoutConfig
	{
	public:
		// For BSP 
		static const int MIN_ROOM_2D_SIZE = 4;
		static const int MAX_ROOM_2D_SIZE = 12;
		static const int ROOM_HEIGHT = 3;
		static constexpr float ROOM_2D_SIZE_BINOMIAL_P = 0.5f;
		static const int ROOM_2D_SIZE_BINOMIAL_MEAN_VALUE = 6;

		const static int VERTICAL_ROOM_LINK_LENGTH = 5;
		const static int VERTICAL_ROOM_LINK_WIDTH = 2;

		Vector3 mapSize; // z is floor count
		float roomDensity; // density such that room count = floor(Rooms generated by BSP * roomDensity)
		float pathLengthCoeff; // coeff such that length of start-end path <= floor(room count * pathLengthCoeff)
		float edgeDensityCoeff; // coeff that defines edge density in graph

		RoomLayoutConfig(const RoomLayoutConfig& other)
			: mapSize(other.mapSize), roomDensity(other.roomDensity), pathLengthCoeff(other.pathLengthCoeff), edgeDensityCoeff(other.edgeDensityCoeff)
		{
		}

		RoomLayoutConfig& operator=(const RoomLayoutConfig& other)
		{
			return RoomLayoutConfig(other);
		}

		RoomLayoutConfig() = default;
	};
}